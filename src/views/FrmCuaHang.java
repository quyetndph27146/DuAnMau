    void loadTable(List<QLCuaHang> list) {
        model.setRowCount(0);
        for (QLCuaHang ch : list) {
            model.addRow(new Object[]{ch.getId(), ch.getMa(), ch.getTen(),ch.getDiaChi(),ch.getThanhPho(),ch.getQuocGia()});
        }
    }
    
    private QLCuaHang validateCuaHang() {
        String id = "";
        String ma = txtMa.getText();
        if (ma.isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được để trống mã");
            return null;
        }
        String ten = txtTen.getText();
        if (ten.isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được để trống tên");
            return null;
        }
        String diaChi = txtDiaChi.getText();
        if (diaChi.isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được để trống Địa chỉ");
            return null;
        }
        String thanhPho = txtThanhPho.getText();
        if (thanhPho.isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được để trống Thành phố");
            return null;
        }
        String quocGia = txtQuocGia.getText();
        if (quocGia.isBlank()) {
            JOptionPane.showMessageDialog(this, "Không được để trống Quốc Gia");
            return null;
        }
        return new QLCuaHang(id, ma, ten,diaChi,thanhPho,quocGia);
    }

