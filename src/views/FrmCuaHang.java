    public List<QLCuaHang> getListByCurPage(){
        totalCuaHangs = iCuaHangService.getAllCuaHang().size();
        totalPages = (totalCuaHangs -1)/pageSize+1;
        lblTong.setText(String.valueOf(totalCuaHangs));
        lblCurrentPage.setText(currentPage+"/"+totalPages);
        int start = (currentPage -1)*pageSize;
        int end = currentPage*pageSize;
        if(end > totalCuaHangs){
            end = totalCuaHangs;
        }
        return iCuaHangService.getAllCuaHang().subList(start, end);
    }
    
    private void clear(){
        txtMa.setText("");
        txtTen.setText("");
        txtDiaChi.setText("");
        txtThanhPho.setText("");
        txtQuocGia.setText("");
    }
