    private void btnTruocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTruocActionPerformed
        // TODO add your handling code here:
        currentPage--;
        if(totalCuaHangs <= pageSize){
            return;
        }
        if(currentPage == 0){
            currentPage = totalPages;
        }
        loadTable(getListByCurPage());
    }//GEN-LAST:event_btnTruocActionPerformed

    private void btnSauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSauActionPerformed
        // TODO add your handling code here:
        currentPage++;
        if(totalCuaHangs <= pageSize){
            return;
        }
        if(currentPage > totalPages){
            currentPage = 1;
        }
        loadTable(getListByCurPage());
    }//GEN-LAST:event_btnSauActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectRow = tblCuaHang.getSelectedRow();
        if (selectRow < 0) {
            JOptionPane.showMessageDialog(this, "Bạn cần chọn 1 Cửa hàng cần update trên bảng");
            return;
        }
        String id = tblCuaHang.getValueAt(selectRow, 0).toString();
        QLCuaHang cuaHang = validateCuaHang();
        if (cuaHang != null) {
            JOptionPane.showMessageDialog(this,iCuaHangService.updateCuaHang(cuaHang,id));
            loadTable(getListByCurPage());
        } 
    }//GEN-LAST:event_btnUpdateActionPerformed

