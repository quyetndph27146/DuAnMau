    private void getAllHD() {
        List<HoaDon> list = iHoaDonRepository.getAllHoasDons();
        cbBoxModel = (DefaultComboBoxModel<String>) cbxMaHD.getModel();
        cbBoxModel.removeAllElements();
        for (HoaDon hd : list) {
            cbBoxModel.addElement(hd.getMa());
        }
    }

    private void getAllChiTietSP() {
        List<String> list = iChitietSPRepository.getAllTen();
        cbBoxModel = (DefaultComboBoxModel<String>) cbxTenSP.getModel();
        cbBoxModel.removeAllElements();
        for (String s : list) {
            cbBoxModel.addElement(s);
        }
    }

    private void loadTable(List<QLHoaDonChiTiet> list) {
        model.setRowCount(0);
        for (QLHoaDonChiTiet hdct : list) {
            model.addRow(new Object[]{hdct.getHoaDon().getMa(), iChitietSPRepository.getTen(hdct.getChiTietSP().getId()),
                hdct.getSoLuong(), hdct.getDonGia(), hdct.getThanhTien()});
        }
    }

    private QLHoaDonChiTiet validateHoaDonChiTiet() {
        String maHD = (String) cbxMaHD.getSelectedItem();
        HoaDon hd = iHoaDonRepository.getHoaDonTheoMa(maHD);
        String tenSP = (String) cbxTenSP.getSelectedItem();
        ChiTietSP chiTietSP = iChitietSPRepository.getChiTietSPTheoTen(tenSP);
        String soLuongString = txtSoLuong.getText();
        if (soLuongString.isBlank()) {
            JOptionPane.showMessageDialog(this, "không đưuọc để trống số lượng");
            return null;
        }
        int soLuong = 0;
        try {
            soLuong = Integer.valueOf(soLuongString);
            if (soLuong <= 0) {
                JOptionPane.showMessageDialog(this, "lỗi dữ liệu,soLuong > 0");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là kiểu số nguyên");
            return null;
        }
        String donGiaString = txtDonGia.getText();
        if (donGiaString.isBlank()) {
            JOptionPane.showMessageDialog(this, "không đưuọc để trống đơn giá");
            return null;
        }
        float donGia = 0;
        try {
            donGia = Float.valueOf(donGiaString);
            if (donGia <= 0) {
                JOptionPane.showMessageDialog(this, "lỗi dữ liệu,donGia > 0");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Đơn giá phải là kiểu số ");
            return null;
        }
        return new QLHoaDonChiTiet(hd, chiTietSP, soLuong, donGia);
    }

