    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectRow = tblHoaDonChiTiet.getSelectedRow();
        if (selectRow < 0) {
            JOptionPane.showMessageDialog(this, "Chon 1 dong ten table");
            return;
        }
        QLHoaDonChiTiet qlhdct = validateHoaDonChiTiet();
        if (qlhdct != null) {
            JOptionPane.showMessageDialog(this, iHoaDonChiTietService.updateHoaDonChiTiet(qlhdct));
            loadTable(getCurrentPage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        QLHoaDonChiTiet hoaDonChiTiet = validateHoaDonChiTiet();
        if (hoaDonChiTiet != null) {
            List<QLHoaDonChiTiet> list = iHoaDonChiTietService.getAllHoaDonChiTiet();
            if(list.size() != 0){
                for(QLHoaDonChiTiet ghChiTiet:list){
                    if(hoaDonChiTiet.getHoaDon().getId().equalsIgnoreCase(ghChiTiet.getHoaDon().getId()) && hoaDonChiTiet.getChiTietSP().getId().equalsIgnoreCase(ghChiTiet.getChiTietSP().getId())){
                        JOptionPane.showMessageDialog(this,"Mã HD và ID CTSP đã tồn tại");
                        return;
                    }
                }
            }
            JOptionPane.showMessageDialog(this, iHoaDonChiTietService.saveHoaDonChiTiet(hoaDonChiTiet));
            loadTable(getCurrentPage());
        }
    }//GEN-LAST:event_btnNewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectRow = tblHoaDonChiTiet.getSelectedRow();
        if (selectRow < 0) {
            JOptionPane.showMessageDialog(this, "Chon 1 dong ten table");
            return;
        }
        int kq = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (kq == JOptionPane.YES_OPTION) {
            String maHD = tblHoaDonChiTiet.getValueAt(selectRow, 0).toString();
            HoaDon hd = iHoaDonRepository.getHoaDonTheoMa(maHD);
            String tenSP = tblHoaDonChiTiet.getValueAt(selectRow, 1).toString();
            ChiTietSP chiTietSP = iChitietSPRepository.getChiTietSPTheoTen(tenSP);
            JOptionPane.showMessageDialog(this, iHoaDonChiTietService.deleteHoaDonChiTiet(hd.getId(), chiTietSP.getId()));
            loadTable(getCurrentPage());
            clear();
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

