    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        QLGioHang qlgh = validateGioHang();
        if (qlgh != null) {
            List<QLGioHang> list = iGioHangService.getAllGioHangs();
            if (list.size() > 0) {
                for (QLGioHang gh : list) {
                    if (qlgh.getMa().equalsIgnoreCase(gh.getMa())) {
                        JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                        return;
                    }
                }
            }
            JOptionPane.showMessageDialog(this, iGioHangService.saveGioHang(qlgh));
            loadTable(getCurrentPage());
        } else {
            JOptionPane.showMessageDialog(this, "Thêm mới thất bại");
        }
    }//GEN-LAST:event_btnNewActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectRow = tblGioHang.getSelectedRow();
        if (selectRow < 0) {
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng trên bảng");
            return;
        }
        QLGioHang qlgh = validateGioHang();
        qlgh.setId(tblGioHang.getValueAt(selectRow, 0).toString());
        if (qlgh != null) {
            JOptionPane.showMessageDialog(this, iGioHangService.updateGioHang(qlgh));
            loadTable(getCurrentPage());
        }else{
            JOptionPane.showMessageDialog(this,"Sửa thất bại");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectRow = tblGioHang.getSelectedRow();
        if (selectRow < 0) {
            JOptionPane.showMessageDialog(this, "Chọn 1 dòng trên bảng");
            return;
        }
        int kq = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (kq == JOptionPane.YES_OPTION) {
            String id = tblGioHang.getValueAt(selectRow, 0).toString();
            JOptionPane.showMessageDialog(this, iGioHangService.deleteGioHang(id));
            clear();
            currentPage = 1;
            loadTable(getCurrentPage());
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

