    private void getCBXKhachHang() {
        cbBoxModel = (DefaultComboBoxModel<String>) cbxKH.getModel();
        List<KhachHang> list = iKhachHangRepository.findAll();
        cbBoxModel.removeAllElements();
        for (KhachHang kh : list) {
            cbBoxModel.addElement(kh.getTen());
        }
    }

    private void getCBXNhanVien() {
        cbBoxModel = (DefaultComboBoxModel<String>) cbxNV.getModel();
        List<NhanVien> list = iNhanVienRepository.findAll();
        cbBoxModel.removeAllElements();
        for (NhanVien nv : list) {
            cbBoxModel.addElement(nv.getTen());
        }
    }

    private void loadTable(List<QLGioHang> gioHangs) {
        model.setRowCount(0);
        for (QLGioHang gh : gioHangs) {
            model.addRow(new Object[]{gh.getId(), gh.getKhachHang().getTen(), gh.getNhanVien().getTen(), gh.getMa(), gh.getNgayTao(),
                gh.getNgayThanhToan(), gh.getTenNguoiNhan(), gh.getDiaChi(), gh.getSdt(), gh.getTinhTrangString()});
        }
    }

